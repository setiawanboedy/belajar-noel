# ==============================Belajar Logika================================

kelas = "Python"
siswa = 2

# Logika if (jika)
if siswa == 2:
    print(f"Kelasnya adalah: {kelas}")


# Logika if dan else (jika atau lainnya)
if siswa >= 2:
    print(f"Kelasnya besar")
else:
    print(f"Kelasnya kecil")


# Logika if atau elif atau else (jika atau jika lainnya atau lainnya)
if siswa < 1:
    print(f"Siswa sedikit sekali")
elif siswa >= 2:
    print(f"Siswa seimbang")
elif siswa !=1:
    print(f"Siswa kurang")
else:
    print(f"Siswa lainnya")          

print("=============================================================================")

